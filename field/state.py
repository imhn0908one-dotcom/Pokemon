from dataclasses import dataclass, field
from random import choice, choices


@dataclass
class SideField:
    """個人の陣地状態を管理するデータクラス。

    このクラスは、攻撃側/防御側のそれぞれのサイドで
    ダメージ計算に影響する設置技や壁、フィールド効果を保持します。
    """

    reflector: bool = False
    """リフレクター状態。

    物理ダメージを受ける際に、ダメージ計算の最終段階で物理防御への補正を適用する。
    物理技に対するダメージ削減を計算する際に参照される。
    """

    light_screen: bool = False
    """ひかりのかべ状態。

    特殊ダメージを受ける際に、ダメージ計算の最終段階で特殊防御への補正を適用する。
    特殊技に対するダメージ削減を計算する際に参照される。
    """

    aurora_veil: bool = False
    """オーロラベール状態。

    物理・特殊ダメージの両方を一定割合軽減する。
    条件が成立する場合、最終防御補正で `reflector`/`light_screen` と同時に扱う。"""

    stealth_rock: bool = False
    """ステルスロックの設置有無。

    交代時に岩タイプのポケモンは無効化され、それ以外のタイプは
    最大HP割合ダメージを受ける。ダメージ計算では交代ダメージ処理に使用する。
    """

    spikes: int = field(default=0, metadata={"min": 0, "max": 3})
    """まきびしの設置枚数（0〜3）。

    交代時に、枚数に応じたダメージを与える。
    計算時には交代ターンの追加ダメージとして参照される。"""

    toxic_spikes: int = field(default=0, metadata={"min": 0, "max": 2})
    """どくびしの設置枚数（0〜2）。

    交代時に相手のポケモンに毒/猛毒状態を付与する。
    ダメージ計算時には毒ダメージを間接的に扱うための状態として参照される。"""

    sticky_web: bool = False
    """ねばねばネット状態。

    交代時に相手の素早さランクを1段階下げる。
    ダメージ計算時には実質的に速度影響を与えるため、素早さ判定・行動順に関連する。
    """

    tailwind: bool = False
    """おいかぜ状態。

    陣地内のポケモンの最終素早さを2倍として扱う。
    ダメージ計算では、行動順や素早さ依存の効果を判定する際に参照される。
    """


@dataclass
class BattleField:
    """バトル全体のフィールド状態を管理するデータクラス。

    このクラスは場全体に影響する天候・フィールドの効果や
    それぞれのプレイヤーサイドの状態をまとめて保持します。
    GUI のフィールド表示やダメージ計算ロジックが直接参照する設計です。
    """

    weather: str = field(
        default="None",
        metadata={"choices": ["None", "Sun", "Rain", "Sandstorm", "Snow"]},
    )
    """天候。ダメージ計算に直接影響するフィールド効果。

    受け付ける値:
        - "None": 天候なし
        - "Sun": はれ（ほのお技威力1.5倍、みず技威力0.5倍）
        - "Rain": あめ（みず技威力1.5倍、ほのお技威力0.5倍）
        - "Sandstorm": すなあらし（いわタイプ以外のポケモンはターン終了時にダメージ、いわタイプは特防1.5倍）
        - "Snow": ゆき（こおりタイプ以外のポケモンはターン終了時にダメージ）
    """

    terrain: str = field(
        default="None",
        metadata={"choices": ["None", "Electric", "Grassy", "Psychic", "Misty"]},
    )
    """フィールド。ダメージ計算や技の追加効果に影響する。

    受け付ける値:
        - "None": フィールドなし
        - "Electric": エレキフィールド（でんき技威力1.3倍、ねむり無効）
        - "Grassy": グラスフィールド（くさ技威力1.3倍、毎ターン回復）
        - "Psychic": サイコフィールド（エスパー技威力1.3倍）
        - "Misty": ミストフィールド（状態異常防止、一部技威力補正）
    """

    trick_room: bool = False
    """トリックルーム状態。

    速度の低いポケモンが先に行動する行動順反転効果。
    ダメージ計算ではなく行動順判定に影響するが、GUI 表示上も重要。
    """

    magic_room: bool = False
    """マジックルーム状態。

    道具の効果を無効化するため、ダメージ計算では持ち物による補正を無視する。
    とつげきチョッキやしんかのきせきなどの防御補正を無視する場として扱う。
    """

    wonder_room: bool = False
    """ワンダールーム状態。

    すべてのポケモンの防御と特防の実数値を入れ替えて計算する。
    物理/特殊のダメージ計算に直接影響する、特別な防御判定フラグ。
    """

    gravity: bool = False
    """じゅうりょく状態。

    飛行/浮遊への地面技命中を可能にし、一部技の命中補正や
    ねむり耐性などを解除する。ダメージ計算時は命中判定側で参照される。
    """

    def reset(self) -> None:
        """フィールド状態をすべて初期値に戻す。

        GUI から「フィールドクリア」操作があった場合に呼び出し、
        すべての場効果とサイド効果をリセットする。
        """
        self.weather = "None"
        self.terrain = "None"
        self.trick_room = False
        self.magic_room = False
        self.wonder_room = False
        self.gravity = False
